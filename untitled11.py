def isPalindrome(text):

    reversedText = ''

    for char in text:

        reversedText = char + reversedText



    if text == reversedText:

        return True

    else:

        return False
print(isPalindrome('level'))  # Вывод: True

print(isPalindrome('hello'))  # Вывод: False

print(isPalindrome('racecar'))  # Вывод: True

