def maximum(a, b, c=None):

    if c is not None:

        if a > b and a > c:

            return a

        elif b > a and b > c:

            return b

        else:

            return c

    else:

        if a > b:

            return a

        else:

            return b
print(maximum(5, 10))         # Вывод: 10

